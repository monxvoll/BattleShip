public class Barcos {
    private Integer posx;
    private Integer posy;


    public Barcos() {
        posx = 0;
        posy = 0;

    }
    public Integer getPosx() {
        return this.posx;
    }

    public void setPosx(Integer newPosx) {
        this.posx =newPosx;
    }

    public Integer getPosy() {
        return this.posy;
    }

    public void setPosy(Integer newPosy) {
        this.posy =newPosy;
    }
}
